//if statements.

var temp = 20;
if (temp<30) {
    console.log('The temperature is low');
}

//Using Both If and Else Statement.

var age = 21;
if (age>20) {
    console.log('You are an adult');
} else {
    console.log('You are not an adult');
}

//Nested If-else-If.

var time = 11;
if (time<10) {
    console.log('Good Morning');
} else if (time<9 ){
    console.log('Good Day');
} else {
    console.log('Good Afternoon');
}