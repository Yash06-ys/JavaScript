//Assignment Operator(=).It assigns the value to every variable.
var x = 10;
var y = 20;
var z = x+y;
console.log(z);

//Addition Operator(+).It is used to add the values stored in the variables.
var a = 20;
var b = 30;
var c = a+b;
console.log(c);

//Multiplication Operator(*).It is used to multiply the values in the variables.
var x = 10;
var y = 20;
var z = x*y;
console.log(z);

//Exponent Operator(**).It is used for squaring the number in the variables.
var x = 10;
var y = 3;
var z = x**y;
console.log(z);

//Division Operator(/).It is used to divide numbers and give us quotient.
var x = 10;
var y = 2;
var z = x/y;
console.log(z);

//Modulus Operator(%).It is used to give us remainder.
var x = 10;
var y = 2;
var z = x%y;
console.log(z);

//Addition Operator is also used for concatenating the strings.
var name1 = 'Yash';
var name2 = 'Shrivastava';
var name = name1+" "+name2;
console.log(name);

//Increment Operator(++).It gives the next value.
var x = 10;
x++;
var z = x;
console.log(z);

//Decrement Operator(--).It gives the previous value.
var x = 10;
--x;
var z = x;
console.log(z);

